///
/// — Linhas em branco fazem a separação dos parágrafos:
///
/// como este exemplo.
///
/// pulando a linha.
///
/// — Para marcar um código em uma linha use `essa sintaxe`.
///
/// — Listas:
/// * Item 1.
/// * Item 1.1
/// * Item 2.
/// * Item 2.1
/// * Item 2.2
/// * Item 3.
///
///
/// Blocos de código são documentados assim:
/// ```
/// this.codigo
/// .contera
/// .esta(formatacao);
/// ```
///
/// A documentação vem setada para Dart como padrão mas
/// você poderá especificar deste modo outras linguagens.
///
/// ```html
/// <h1>HTML é mágico!</h1>
/// ```
///
/// Os links ficam desta forma:
///
/// * http://www.google.com
/// * [com a URL na linha](http://google.com)
/// * [ou separada][ref link]
