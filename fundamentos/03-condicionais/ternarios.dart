/*
  * Condicional if else ternario
  - é uma forma mais enxuta de escrever um comando if else

  * sintaxe
  var x = condição ? valor A : valor B
  Se a condição for verdadeira x vai receber A, caso contrário x vai receber B
*/

main() {
  bool condition = true;
  condition ? print('Verdadeiro') : print('False');
}
